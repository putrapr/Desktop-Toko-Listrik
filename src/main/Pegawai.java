    private void tabelPegawai(){
        Object[] Baris = {"Nama", "No. Telepon", "Alamat"};
        tabmode = new DefaultTableModel(null, Baris);
        tbl_pegawai.setModel(tabmode);
        String query = "SELECT nama_peg, telp_peg, alamat_peg FROM pegawai";
        try {
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(query);
            while (hasil.next()){                
                String a = hasil.getString("nama_peg");                
                String b = hasil.getString("telp_peg");
                String c = hasil.getString("alamat_peg");
                String[] data={a,b,c};
                tabmode.addRow(data);
            }            
        } catch (SQLException e) {
            pesanSQLPopUp("Data tabel pegawai gagal ditampilkan",e);
        }
    } 
    
