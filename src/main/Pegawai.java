    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        String nama = tf_nama.getText();
        String telp = tf_no_telp.getText();
        Date cast_tgl = (Date) date_tgl_lahir.getDate();
        if (nama.equals("")) pesanPopUp("Nama Kosong !. \nIsi terlebih dahulu.");
        else if (telp.equals("")) pesanPopUp("No. Telepon Kosong !. \nIsi terlebih dahulu.");
        else if (cast_tgl == null) pesanPopUp("Tanggal Kosong !.\nIsi terlebih dahulu.");
        else if (adaNamaTelp(nama,telp)) pesanPopUp("Nama dan No. Telepon Sudah Ada !");
        else {
            String query = "INSERT INTO pegawai VALUES (?,?,?,?,?,?,?)";
            try{
                PreparedStatement stat = conn.prepareStatement(query);
                stat.setString(1, null);
                stat.setString(2, nama);
                stat.setString(3, tf_tmpt_lahir.getText());
                
                SimpleDateFormat Date_Format = new SimpleDateFormat("yyyy-MM-dd"); 
                String strDate = Date_Format.format(cast_tgl);
                stat.setString(4, strDate); 

                String jkel="";
                if(rb_pria.isSelected()) jkel="Pria";
                else jkel="Wanita";            
                stat.setString(5, jkel);
                stat.setString(6, ta_alamat.getText());
                stat.setString(7, telp);
                
                stat.executeUpdate();
                bersihkanMasukan();            
                tabelPegawai();
            } catch (SQLException e){
                pesanSQLPopUp("Data tidak bisa disimpan",e);
            }
        }             
    }//GEN-LAST:event_btn_simpanActionPerformed

    private boolean adaNamaTelp(String pnama, String ptelp){
        String query = "SELECT nama_peg FROM pegawai WHERE nama_peg = '"+pnama+"'";
        try {
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(query);
            if (hasil.next()){  // Jika nama ada                  
                query = "SELECT telp_peg FROM pegawai WHERE telp_peg = '"+ptelp+"'";
                hasil = stat.executeQuery(query);
                if (hasil.next()) return true; // Jika telp ada                    
            }
        } catch (SQLException e) {
            pesanSQLPopUp("Gagal cek nama telp",e);
        }
        return false;
    }       
    
