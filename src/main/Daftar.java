    private void errorKedip(JLabel label, String pesan){
        label.setVisible(false);
        Timer timer = new Timer(500, event -> {
                label.setVisible(true);
                label.setText(pesan);
            });
        timer.setRepeats(false);
        timer.start();
    }
    
    private void btn_cekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cekActionPerformed
        String kode = tf_kodeAktivasi.getText();
        if (kode.equals("")) errorKedip(lb_kodeSalah,"Kode Kosong !");
        else {
            String sql = "SELECT * FROM aktivasi WHERE kode_aktivasi = '"+kode+"'";
            try {
                Statement stat = conn.createStatement();
                ResultSet hasil = stat.executeQuery(sql);
                if (hasil.next()){
                    int id = hasil.getInt("id_aktivasi");
                    sql = "SELECT * FROM admin WHERE aktivasi_id = '"+id+"'";
                    hasil = stat.executeQuery(sql);
                    if (hasil.next()) errorKedip(lb_kodeSalah,"Kode Sudah Dipakai !");
                    else tampilDaftar();
                } else errorKedip(lb_kodeSalah,"Kode Salah !");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Gagal cek Kode Aktivasi : "+e);
            }
        }   
    }//GEN-LAST:event_btn_cekActionPerformed
    
