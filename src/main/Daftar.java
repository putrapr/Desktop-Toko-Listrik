    private void btn_daftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_daftarActionPerformed
        // Cek: nama, sandi, email kosong & Nama sudah ada
        String nama = tf_nama.getText();
        String sandi = String.valueOf(tf_sandi.getPassword());
        String email = tf_email.getText();
        if (nama.equals("")) errorKedip(lb_daftarSalah,"Nama Kosong !");
        else if (sandi.equals("")) errorKedip(lb_daftarSalah,"Sandi Kosong !");
        else if (email.equals("")) errorKedip(lb_daftarSalah,"Email Kosong !");
        else if (cekNama(nama)) errorKedip(lb_daftarSalah,"Nama Sudah Ada !");
        else {
            // Tambahkan akun baru ke database
            String kodeAktivasi = tf_kodeAktivasi.getText();
            int idAktivasi = ambilId(kodeAktivasi);           
            buatAkun(idAktivasi, nama, sandi, email);
            this.dispose();
        }        
    }//GEN-LAST:event_btn_daftarActionPerformed

    private boolean cekNama(String pnama){
        boolean out = false;
        String nama = "";
        String sql = "SELECT nama_admin FROM admin WHERE nama_admin = '"+pnama+"'";
        try {
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            if (hasil.next()){
                nama = hasil.getString("nama_admin");
                if (nama.equals(pnama)) out = true;
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal ambil nama : "+e);
        }
        return out;
    }

    private void buatAkun (int pidAktivasi, String pnama, String psandi, String pemail) {
        String sql = "INSERT INTO admin VALUES (?,?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, pnama);
            stat.setInt(2, pidAktivasi);
            stat.setString(3, psandi);
            stat.setString(4, pemail);
            stat.setString(5, "AA");
            stat.setString(6, "ZZZ");
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Akun Berhasil Dibuat !");
        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Gagal membuat akun : "+e);
        }
    }
    
    private int ambilId (String pkode){
        int id = 0;
        String sql = "SELECT id_aktivasi FROM aktivasi WHERE kode_aktivasi = '"+pkode+"'";
        try {
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            if (hasil.next()){
                id = hasil.getInt("id_aktivasi");
            }            
        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Gagal ambil id aktivasi : "+e);
        }
        return id;
    }
    
