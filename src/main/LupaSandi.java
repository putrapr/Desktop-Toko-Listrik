    private void btn_simpanSandiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanSandiActionPerformed
        String sandiBaru = String.valueOf(tf_sandiBaru.getPassword());
        String konfirSandi = String.valueOf(tf_konfirSandi.getPassword());   
        if (sandiBaru.equals("")) errorKedip(lb_salahSandi, "Sandi Baru Kosong !");
        else if (konfirSandi.equals("")) errorKedip(lb_salahSandi, "Konfirmasi Sandi Kosong !");
        else if (!(sandiBaru.equals(konfirSandi))) errorKedip(lb_salahSandi, "Sandi Baru dan Konfirmasi Sandi tidak cocok !");
        else {
            String nama = tf_nama.getText();
            ubahSandi(sandiBaru, nama);
            this.dispose();
        }
    }//GEN-LAST:event_btn_simpanSandiActionPerformed

    private void ubahSandi(String psandiBaru, String pnama){
        String sql = "UPDATE admin SET sandi_admin=? WHERE nama_admin=?";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, psandiBaru);
            stat.setString(2, pnama);
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Sandi Berhasil Diubah !");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal Ubah Sandi");
        }
    }
    
