    private void btn_cekNamaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cekNamaActionPerformed
        String nama = tf_nama.getText();
        if (nama.equals("")) errorKedip(lb_salahNama, "Nama Kosong !");
        else {
            boolean namaAda = cekNama(nama);
            if (namaAda) {
                lb_salahNama.setVisible(false);
                lb_benarNama.setVisible(true);
                panel_email.setVisible(true);
                panel_nama.setBackground(new java.awt.Color(211,212,171));
                tf_nama.setEnabled(false);
                btn_cekNama.setEnabled(false);
                lb_nama.setEnabled(false);
            } else errorKedip(lb_salahNama, "Nama Salah !");
        }        
    }//GEN-LAST:event_btn_cekNamaActionPerformed

    private void errorKedip(JLabel label, String pesan){
        label.setVisible(false);
        Timer timer = new Timer(500, event -> {
                label.setVisible(true);
                label.setText(pesan);
            });
        timer.setRepeats(false);
        timer.start();
    }
    
    private boolean cekNama(String pnama) {
        String sql = "SELECT nama_admin FROM admin WHERE nama_admin = '"+pnama+"'";
        try {
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            if (hasil.next())return true;
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal ambil nama admin : "+e);
        }
        return false;
    }
    
    private void btn_okEmailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_okEmailActionPerformed
        String nama = tf_nama.getText();
        String email = tf_email.getText();
        if (email.equals("")) errorKedip(lb_salahEmail, "Email Kosong !");
        else {
            boolean emailBenar = cekEmail(nama, email);
            if (emailBenar){
                lb_salahEmail.setVisible(false);
                lb_benarEmail.setVisible(true);
                panel_sandi.setVisible(true);
                panel_email.setBackground(new java.awt.Color(211,212,171));
                tf_email.setEnabled(false);
                btn_okEmail.setEnabled(false);
                lb_email.setEnabled(false);
            } else errorKedip(lb_salahEmail, "Email Salah !");
        }
    }//GEN-LAST:event_btn_okEmailActionPerformed

    private boolean cekEmail (String pnama, String pemail){
        String sql = "SELECT * FROM admin WHERE "
                   + "nama_admin = '"+pnama+"' AND email_admin = '"+pemail+"'";
        try {
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            if (hasil.next()) return true;
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal Cek email: "+e);
        }
        return false;
    }
    
